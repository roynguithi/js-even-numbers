// Start a loop with i initialized to 0, running until i is 20, incrementing i by 1 in each iteration
for (let i = 0; i <=20; i++) {
    // Check if the current value of i is even (i.e., divisible by 2 with no remainder)
    if (i%2 === 0) {
         // Print the even number to the console
        console.log(i);
    };
}